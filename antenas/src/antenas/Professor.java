package antenas;

public class Professor {
	private String email;
	private String senha;

	public String getEmail() {
		return this.email;
	}

	public String setEmail(String value) {
		return this.email = value;
	}

	public String getSenha() {
		return this.senha;
	}

	public String setSenha(String value) {
		return this.email = value;
	}
}
